<h4>Business Analysis</h4>
<p>BCS-ISEB certifications et qualifications fournissent la validation des compétences proffessionnelles de l'informatique. Après un exercice de rebranding, la British Computer Society est maintenant allusion à ses certifications professionnelles que BCS plutôt que ISEB. Les groupes BCS ses certifications dans 10 domaines de base :</p>
<ul>
	<li>Analyse d’affaires</li>
	<li>Solution Development et Architecture</li>
	<li>Software Testing</li>
	<li>IT & Service Management</li>
	<li>Software Asset Management</li>
	<li>IS Consultancy</li>
	<li>Gouvernance IT et sécurité de l’information</li>
	<li>Sustainable IT</li>
	<li>Gestion de projet et de soutien (BSPP)</li>
	<li>Centre de gestion des données</li>
</ul>
<p><span>Types de BCS-ISEB certifications et qualifications</span>
	Il existe une gamme de certificats et de formation accrédité associés à chaque zone. Ils sont positionnés à la « Foundation, Practitioner and Higherlevels ». Ils sont mappés sur le cadre de compétences SFIAplus pour mapper des voies de développement dans chaque discipline. Les qualifications de niveau supérieur comprennent des diplômes et des certificats de gestion qui sont destinés aux spécialistes et gestionnaires ; ils nécessitent une vue en coupe de composants modulaires qui démontrent la largeur (ainsi que la profondeur) de la compréhension. Les diplômes les plus populaires sont en analyse d’affaires et développement des solutions. Pour la gestion des services, BCS agit comme une autorité d’examen ITIL - sous les auspices du Groupe APM. Pour Software Testing, BCS agit comme une autorité d’examen pour les qualifications ISTQB (même si elle propose également sa propre qualification intermédiaire). La certification de mise au point se concentre sur les brevets de niveau supérieur plutôt que ceux obtenus par le succès avec un seul cours.</p>

