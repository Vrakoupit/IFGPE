<h4>VMware</h4>
<p>Les certifications VMware vous marquent comme un expert dans les technologies de la virtualisation. VMware offre des certifications de classe mondiale destinées à valider et reconnaître les professionnels avec les capacités techniques et de l’expérience du monde réel nécessaires pour installer, gérer, déployer et soutenir les produits et solutions VMware. Les certifications VMware sont disponibles dans deux domaines de la virtualisation : Datacenter et Desktop. Chaque spécialité est décomposée en trois niveaux de certification distincte réfléchissante de compétence et niveau de compétence :</p>
Datacenter Virtualization
<ul>
	<li>VMware Certified Professional (VCP-DCV)</li>
	<li>VMware Certified Advanced Professional (VCAP)</li>
	<li>VMware Expert Design Certifié (VCDX)</li>
</ul>
Desktop Virtualization
<ul>
	<li>VMware Certified Associate (VCA-DT)</li>
	<li>VMware Certified Professional (VCP-DT)</li>
	<li>VMware Certified Advanced Professional (VCAP-DT)</li>
</ul>
<p>La certification VMware la plus populaire est VCP-DCV.</p>

