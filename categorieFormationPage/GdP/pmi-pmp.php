<h4>PMI - PMP</h4>
<p>Les certifications PMI de l’Institut de gestion de projet sont reconnues dans le monde entier.</p>
<p>Le Project Management Institute (PMI) est américain et est la plus grande association au monde pour la profession de gestion de projet. Elle administre une hiérarchie des certifications. Obtenir un titre professionnel par le biais PMI signifie que l’on a :</p>
<ul>
	<li>Démontrer l’éducation appropriée et / ou une expérience professionnelle</li>
	<li>Passé un examen rigoureux</li>
	<li>Est convenu de respecter un code de déontologie</li>
	<li>Soucieuse de maintenir leur accréditation active à travers les exigences de certification réunion poursuivies</li>
</ul>
<p><span>Il y a 3 certifications PMI</span>, qui montre un niveau éprouvé de la formation, les connaissances et l’expérience en gestion de projet. Ils sont disponibles pour les membres de l’Institut et les non-membres. Les examens sont administrés par Prometric.</p>
<p><span>Certified Associate in Project Management (CAPM)</span>Le titre de CAPM peut bénéficier d’un large éventail de membres de l’équipe qui veulent augmenter leur performance et les compétences des équipes de projet en acquérant des connaissances sur les processus de gestion de projet et de la terminologie.</p>

<p><span>Project Management Professional (PMP)</span>
Une désignation mondialement reconnue qui sert de base à partir de laquelle vous pouvez pratiquer avec compétence en tant que chef de projet, mener et diriger les tâches du projet. PGR sont en phase avec les dernières tendances dans la profession de gestion de projet, et ils ont démontré leur engagement envers la profession.</p>

<p><span>Programme Management Professional (PgMP)</span>
Nouveau titre PgMP de PMI est spécifiquement développé pour améliorer les qualifications du professionnel qui dirige la gestion coordonnée de multiples projets et assure le succès final d’un programme.</p>

<p>Afin de satisfaire les exigences de certification Programme de maintien (CCR) et de maintenir un individu en état des titres de compétences actives qui ont atteint le PMP et / ou programme de gestion professionnelle doit courir et rapporter un minimum de 60 unités de formation continue (UFC) au cours de chaque cycle de CCR, qui est généralement de 3 ans, remplir une demande de renouvellement de la certification et de paiement de la taxe de renouvellement, et de réaffirmer le Code de PMI éthique et de déontologie.</p>

