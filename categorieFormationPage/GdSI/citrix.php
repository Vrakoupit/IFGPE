<h4>Citrix</h4>
<p>Les certifications Citrix sont conçues pour permettre aux professionnels de l’informatique pour développer et valider leur capacité par rapport aux technologoes Citrix actuellement utilisés par plus de 300 000 organisations à travers le monde. Citrix a restructuré leurs attestations à des solutions fondées.</p>
<span>Associé</span>
<ul>
	<li>Citrix CertifiedAssociate - Applications Android et de bureau (CCA-AD)</li>
	<li>Citrix Certified Associate - Networking (CCA-N)</li>
</ul>

<span>Professionnel</span>
<ul>
	<li>Citrix Certified Professional - Applications Android et de bureau (PCC-AD)</li>
	<li>Citrix Certified Professional - Networking (PCC-N)</li>
	<li>Citrix Certified Professional - Mobilité (PCC-M)</li>
</ul>

<span>Expert</span>
<ul>
	<li>Citrix Certified Expert - Applications Android et de bureau (CCE-AD) Divers chemins, y compris Application Virtualization, Desktop Virtualization, Application Networking et la virtualisation des serveurs sont disponibles, permettant aux professionnels IT d’aligner leurs certifications avec les solutions technologiques les plus pertinents.</li>
	<li>Les professionnels de l’informatique peuvent réaliser des désignations spécifiques pour les derniers produits Citrix, tels que XenApp, NetScaler, Desktop, et XenServerafin de prouver leur compétence et leur savoir faire sur les derniers produits de l’entreprise.</li>
</ul>